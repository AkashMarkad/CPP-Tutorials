// Maths : 

#include <iostream>
#include <cmath>        //Include the cmath library
using namespace std;

int main() {

    // max and min 
    cout << max(10 , 20) << endl;
    cout << min(10 , 20) << endl;

    cout << sqrt(81) << endl;       //square root
    cout << round(3.63) << endl;    //rounds a number
    cout << log(2) << endl;         //natural logarithm

    cout << abs(12.5) << endl;        //absolute value
    cout << cos(45) << endl;          //cosine value
    cout << sin(45) << endl;          //sine value
    cout << pow(2 , 3) << endl;       //value of 2 to the power of 3
    return 0;
}